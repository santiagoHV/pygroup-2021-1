#Operadores aritmeticos
a = 5
b = 3

print(a + b)
print(a - b)
print(a * b)
print(a / b)

print(a % b)#Modulo retorna el residuo de una division
print(a ** b)#potencia
print(a // b)#division entera


#operadores relacionales
print(f'mayor que   {a > b}')
print(f'menor que   {a < b}')
print(f'igual que   {a == b}')
print(f'mayor o igual que   {a >= b}')
print(f'menor o igual que   {a <= b}')
print(f'diferente que   {a != b}')

print(a > b or a == b)
print(a > b and a == b)